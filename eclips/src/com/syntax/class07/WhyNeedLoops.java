package com.syntax.class07;

public class WhyNeedLoops {

	public static void main(String[] args) {
		
		//  я хочу что-то сказать 4 раза
		
		int a=1;
		
		
		
		if (a<4)  {
			System.out.println("Привет");
		}
		
		System.out.println("----------------------");
		
		while (a<4)  {
			System.out.println("Привет");
			a++;
		}
		
		

	}

}
