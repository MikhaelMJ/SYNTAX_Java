package com.syntax.class07;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i=1;
		
		while(i>7) {
			System.out.println("hello");
			i++;
		}
		
		System.out.println("-----do While-----");
		
		int j=1;
		
		do {
			System.out.println("Hello");
			j++;
		}while(j>=3);
		
		System.out.println("----------------------------");
		
		int n=1;
		
		do {
			System.out.println(n);
			n++;
		}while(n<=10);

	}

}
